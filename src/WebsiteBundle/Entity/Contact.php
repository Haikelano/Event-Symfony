<?php

namespace WebsiteBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Contact
 *
 * @ORM\Table(name="contact")
 * @ORM\Entity
 */
class Contact
{
    /**
     * @var integer
     *
     * @ORM\Column(name="idcontact", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $idcontact;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=45, nullable=true)
     */
    private $name;

    /**
     * @var string
     *
     * @ORM\Column(name="email", type="string", length=45, nullable=true)
     */
    private $email;

    /**
     * @var string
     *
     * @ORM\Column(name="subject", type="string", length=45, nullable=true)
     */
    private $subject;

    /**
     * @var string
     *
     * @ORM\Column(name="message", type="string", length=500, nullable=true)
     */
    private $message;

    /**
     * @var \DateTime
     *
     * @ORM\Column(name="date", type="date", nullable=true)
     */
    private $date;

    /**
     * @var string
     *
     * @ORM\Column(name="phone", type="string", length=45, nullable=true)
     */
    private $phone;

    /**
     * @var string
     *
     * @ORM\Column(name="lastname", type="string", length=45, nullable=true)
     */
    private $lastname;

    /**
     * @var string
     *
     * @ORM\Column(name="civility", type="string", length=45, nullable=true)
     */
    private $civility;

    /**
     * @var boolean
     *
     * @ORM\Column(name="istreated", type="boolean", nullable=true)
     */
    private $istreated;

    /**
     * @var string
     *
     * @ORM\Column(name="companyAdress", type="string", length=100, nullable=true)
     */
    private $companyadress;

    /**
     * @var string
     *
     * @ORM\Column(name="companyPhone", type="string", length=45, nullable=true)
     */
    private $companyphone;

    /**
     * @var string
     *
     * @ORM\Column(name="companyMail", type="string", length=45, nullable=true)
     */
    private $companymail;

    /**
     * @return int
     */
    public function getIdcontact()
    {
        return $this->idcontact;
    }

    /**
     * @param int $idcontact
     */
    public function setIdcontact($idcontact)
    {
        $this->idcontact = $idcontact;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

    /**
     * @return string
     */
    public function getSubject()
    {
        return $this->subject;
    }

    /**
     * @param string $subject
     */
    public function setSubject($subject)
    {
        $this->subject = $subject;
    }

    /**
     * @return string
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param string $message
     */
    public function setMessage($message)
    {
        $this->message = $message;
    }

    /**
     * @return \DateTime
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * @param \DateTime $date
     */
    public function setDate($date)
    {
        $this->date = $date;
    }

    /**
     * @return string
     */
    public function getPhone()
    {
        return $this->phone;
    }

    /**
     * @param string $phone
     */
    public function setPhone($phone)
    {
        $this->phone = $phone;
    }

    /**
     * @return string
     */
    public function getLastname()
    {
        return $this->lastname;
    }

    /**
     * @param string $lastname
     */
    public function setLastname($lastname)
    {
        $this->lastname = $lastname;
    }

    /**
     * @return string
     */
    public function getCivility()
    {
        return $this->civility;
    }

    /**
     * @param string $civility
     */
    public function setCivility($civility)
    {
        $this->civility = $civility;
    }

    /**
     * @return bool
     */
    public function isIstreated()
    {
        return $this->istreated;
    }

    /**
     * @param bool $istreated
     */
    public function setIstreated($istreated)
    {
        $this->istreated = $istreated;
    }

    /**
     * @return string
     */
    public function getCompanyadress()
    {
        return $this->companyadress;
    }

    /**
     * @param string $companyadress
     */
    public function setCompanyadress($companyadress)
    {
        $this->companyadress = $companyadress;
    }

    /**
     * @return string
     */
    public function getCompanyphone()
    {
        return $this->companyphone;
    }

    /**
     * @param string $companyphone
     */
    public function setCompanyphone($companyphone)
    {
        $this->companyphone = $companyphone;
    }

    /**
     * @return string
     */
    public function getCompanymail()
    {
        return $this->companymail;
    }

    /**
     * @param string $companymail
     */
    public function setCompanymail($companymail)
    {
        $this->companymail = $companymail;
    }


}
